create database institute;
use institute;
create table student(std_id int,std_name varchar(15),std_dob date,std_gender enum("male","female"),std_email varchar(20),std_joindate date,std_address varchar(50),std_education varchar(20),std_courseid int,std_branchid int,std_contact varchar(10));
create table course(course_id int,course_name varchar(15));
create table placement(std_id int,placed_company varchar(10),placement_ctc varchar(10),placement_remarks varchar(40));
create table branch(branch_id int,branch_name varchar(15));
create table payment(std_id int,payment_amount float,payment_pending float,payment_date timestamp);
alter table student add constraint pk primary key(std_id),add constraint uk unique(std_contact);
alter table course add constraint pk primary key(course_id);
alter table branch add constraint spk primary key(branch_id);
alter table payment add constraint payment_fk foreign key(std_id) references student(std_id);
alter table placement add constraint placement_fk foreign key(std_id) references student(std_id);
alter table student add foreign key(std_courseid) references course(course_id);
alter table student add foreign key(std_branchid) references branch(branch_id); 
describe student;
alter table student modify column std_id int auto_increment;
alter table placement modify column placement_remarks varchar(10) default "good";
alter table payment modify column payment_date timestamp default current_timestamp on update current_timestamp;

